#!/usr/bin/env node
// Reads business.config.json, updates BUSINESS fields in src/config.ts,
// and prints env vars to paste into Render dashboard.
import { readFile, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';

const CONFIG_JSON = 'business.config.json';
const CONFIG_TS = 'artifacts/trades-template/src/config.ts';
const ROBOTS_TXT = 'artifacts/trades-template/public/robots.txt';

if (!existsSync(CONFIG_JSON)) {
  console.error(`✗ Missing ${CONFIG_JSON}`);
  process.exit(1);
}
if (!existsSync(CONFIG_TS)) {
  console.error(`✗ Missing ${CONFIG_TS}`);
  process.exit(1);
}

const biz = JSON.parse(await readFile(CONFIG_JSON, 'utf8'));
let src = await readFile(CONFIG_TS, 'utf8');

// Warn early if biz.tradeType doesn't match any key in placeholders.ts.
// An unmatched trade renders generic gray "Add Photo" cards — fine for
// pitching, bad for shipping. Catch it here so the operator picks a
// curated trade (or adds a new entry) before the build runs.
await warnIfUnknownTradeType(biz.tradeType);

// Map: JSON key → config.ts field name
const fieldMap = {
  name: biz.businessName,
  email: biz.email,
  phone: biz.phone,
  address: biz.address,
  tradeType: biz.tradeType,
  tagline: biz.tagline,
};

let updated = 0;
for (const [key, value] of Object.entries(fieldMap)) {
  if (value === undefined || value === null) continue;
  const escaped = String(value).replace(/"/g, '\\"');
  // Match: keyName: "anything" OR keyName: 'anything' OR keyName: `anything`
  const re = new RegExp(`(\\b${key}\\s*:\\s*)(["'\`])([^"'\`\\n]*)\\2`);
  if (re.test(src)) {
    src = src.replace(re, `$1"${escaped}"`);
    updated++;
  }
}

// THEME.primary — convert biz.primaryColor (hex) → "h s% l%" HSL triple
if (biz.primaryColor && biz.primaryColor.trim() && biz.primaryColor.trim().toLowerCase() !== '#000000') {
  const hsl = hexToHslTriple(biz.primaryColor.trim());
  if (hsl) {
    const re = new RegExp(`(\\bprimary\\s*:\\s*)(["'\`])([^"'\`\\n]*)\\2`);
    if (re.test(src)) {
      src = src.replace(re, `$1"${hsl}"`);
      updated++;
    }
  }
}

await writeFile(CONFIG_TS, src);
console.log(`✓ Updated ${updated} field(s) in ${CONFIG_TS}`);

// Sync robots.txt sitemap URL from biz.domain
if (existsSync(ROBOTS_TXT)) {
  const robots = await readFile(ROBOTS_TXT, 'utf8');
  if (biz.domain && biz.domain.trim()) {
    const next = robots.replace(/\{\{DOMAIN\}\}/g, biz.domain.trim());
    await writeFile(ROBOTS_TXT, next);
    console.log(`✓ robots.txt updated with ${biz.domain.trim()}`);
  } else if (robots.includes('{{DOMAIN}}')) {
    console.warn(`⚠ biz.domain is empty — leaving {{DOMAIN}} placeholder in ${ROBOTS_TXT}. Set "domain" in ${CONFIG_JSON} and re-run.`);
  }
}

// Print env vars for Render
console.log('');
console.log('────────────────────────────────────────────────────');
console.log('Paste these into Render → service → Environment:');
console.log('────────────────────────────────────────────────────');
console.log(`LEAD_NOTIFY_TO=${biz.leadNotifyTo || 'teddy.nk28@gmail.com'}`);
if (biz.domain) {
  console.log(`ALLOWED_ORIGINS=https://${biz.domain},https://www.${biz.domain}`);
} else {
  console.log('# ALLOWED_ORIGINS — set after domain is live');
}
console.log('────────────────────────────────────────────────────');
console.log('');
console.log('Note: ADMIN_PASSWORD + SESSION_SECRET are auto-generated by render.yaml.');
console.log('      DATABASE_URL is set by scripts/create-neon-project.sh.');

// Parses placeholders.ts to extract the set of known trade keys (canonical
// keys + aliases). Falls back to a hardcoded list if the file can't be
// parsed — better to skip the warning than crash sync.
async function warnIfUnknownTradeType(tradeType) {
  if (!tradeType || typeof tradeType !== 'string') return;
  const key = tradeType.toLowerCase().trim();
  if (!key) return;

  const placeholdersPath = 'artifacts/trades-template/src/placeholders.ts';
  if (!existsSync(placeholdersPath)) return;

  const file = await readFile(placeholdersPath, 'utf8');
  const known = new Set();

  // Match canonical keys from TRADE_PLACEHOLDERS: e.g.   roofing: {  or  "lawn-care": {
  const blockMatch = file.match(/TRADE_PLACEHOLDERS\s*:\s*Record<[^>]+>\s*=\s*\{([\s\S]*?)\n\};/);
  if (blockMatch) {
    for (const m of blockMatch[1].matchAll(/^\s*"?([a-z0-9_-]+)"?\s*:\s*\{/gim)) {
      if (m[1] !== 'default') known.add(m[1].toLowerCase());
    }
  }

  // Match aliases from TRADE_ALIASES: e.g.   plumber: "plumbing",
  const aliasMatch = file.match(/TRADE_ALIASES\s*:\s*Record<[^>]+>\s*=\s*\{([\s\S]*?)\n\};/);
  if (aliasMatch) {
    for (const m of aliasMatch[1].matchAll(/^\s*"?([a-z0-9_-]+)"?\s*:\s*"[a-z0-9_-]+"/gim)) {
      known.add(m[1].toLowerCase());
    }
  }

  if (known.size === 0) return; // parse failure — skip silently

  if (!known.has(key)) {
    const suggestions = ['salon', 'barber', 'fitness', 'roofer', 'cleaning', 'painter', 'plumber'];
    console.warn('');
    console.warn(`⚠ tradeType "${tradeType}" not in placeholders.ts — site will show generic placeholders. Add to placeholders.ts or pick: ${suggestions.join(', ')}, etc.`);
    console.warn('');
  }
}

// Convert "#rrggbb" → "h s% l%" (the HSL triple format THEME.primary uses).
// Returns null if the input isn't a valid 6-digit hex string.
function hexToHslTriple(hex) {
  const m = /^#?([0-9a-f]{6})$/i.exec(hex);
  if (!m) return null;
  const n = parseInt(m[1], 16);
  const r = ((n >> 16) & 255) / 255;
  const g = ((n >> 8) & 255) / 255;
  const b = (n & 255) / 255;
  const max = Math.max(r, g, b);
  const min = Math.min(r, g, b);
  const l = (max + min) / 2;
  let h = 0;
  let s = 0;
  if (max !== min) {
    const d = max - min;
    s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
    switch (max) {
      case r: h = (g - b) / d + (g < b ? 6 : 0); break;
      case g: h = (b - r) / d + 2; break;
      case b: h = (r - g) / d + 4; break;
    }
    h *= 60;
  }
  return `${Math.round(h)} ${Math.round(s * 100)}% ${Math.round(l * 100)}%`;
}
